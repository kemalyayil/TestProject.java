package Day41._1_Abstraction;

public abstract class AbstractClass {               // abstract key word.  in these classes ,
                                                    // we are not able to create instance and objects

    //public abstract void  abstractMethod(){}      abstract methods can not have body{}

    public abstract void  abstractMethod();

    // in public class we can not create an abstract method

    /*
    1- Abstract classes cannot be created objects
    2- Abstract classes can have both abstract and non-abstract methods
    3- Abstract methods don't have body but will require when inherited
     */

}
